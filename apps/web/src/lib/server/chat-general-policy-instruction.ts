import { formatArithmeticExpression } from './chat-general-policy-intent';
import type { GeneralChatIntent } from './chat-general-policy.types';

export function buildGeneralChatIntentInstruction(intent: GeneralChatIntent): string {
  if (intent.kind === 'greeting') {
    return [
      '当前用户命中了“新会话问候语”高频入口。',
      '请像真实 AI 产品一样自然回复，不要显得像固定模板。',
      '回复要求：',
      '1. 先用一句话完成自我介绍，明确你是前端求职、简历优化、模拟面试方向的助手。',
      '2. 再用 3 条以内短列表说明你能提供的帮助。',
      '3. 最后明确给出下一步动作，引导用户直接贴简历、目标岗位或具体前端问题。',
      '4. 语气专业、自然，不要说自己“超出职责范围”。',
    ].join('\n');
  }

  if (intent.kind === 'resume_optimize') {
    return [
      '当前用户命中了“简历优化入口”高频意图，但尚未提供简历正文。',
      '请自然地告诉用户你可以帮他优化简历，但不要直接开始泛泛点评。',
      '回复要求：',
      '1. 先确认你可以帮助优化简历。',
      '2. 明确要求用户粘贴完整简历文本，并提醒隐藏姓名、电话、邮箱等敏感信息。',
      '3. 可以补充你会重点关注的 3 到 5 个评审维度，例如岗位匹配度、技术栈深度、项目亮点、量化结果。',
      '4. 回复要像真实产品助手，不要机械复读，不要输出 Markdown 分割线。',
    ].join('\n');
  }

  if (intent.kind === 'simple_arithmetic') {
    return [
      '当前用户问题属于轻度跑题的简单算术题。',
      `你必须先直接回答：${formatArithmeticExpression(intent.arithmetic)}`,
      '回答完之后，再自然补一句：你更擅长帮助用户处理前端技术问答、简历优化或模拟面试。',
      '不要生硬拒绝，不要长篇展开，整体控制在 2 段以内。',
    ].join('\n');
  }

  if (intent.kind === 'self_intro') {
    return [
      '当前用户命中了“前端面试自我介绍”高频意图。',
      '请像专业面试教练一样回复，不要直接给空泛鸡汤。',
      '回复要求：',
      '1. 先说明你可以按前端面试场景帮助打磨自我介绍。',
      '2. 先向用户索取 3 类关键信息：求职状态、目标岗位级别、年限和核心技术栈。',
      '3. 再给一版简洁但可执行的结构化建议，告诉用户如何在 60 到 90 秒内完成表达。',
      '4. 最后给出下一步动作，例如“发一版自我介绍，我帮你点评”或“贴经历，我帮你改写”。',
    ].join('\n');
  }

  if (intent.kind === 'technical_question') {
    const structureLines =
      intent.style === 'comparison'
        ? [
            '4. 使用以下结构组织回答：`## 一句话区别`、`## 核心差异`、`## 什么时候用`、`## 示例`、`## 面试里怎么回答`。',
            '5. 可以用简短表格或短列表做对比，但不要为了排版堆表格。',
          ]
        : intent.style === 'mechanism'
          ? [
              '4. 使用以下结构组织回答：`## 核心结论`、`## 执行流程或工作原理`、`## 示例`、`## 常见追问`、`## 面试回答建议`。',
              '5. 如果涉及执行顺序或调度机制，优先用有编号的步骤解释，不要输出 mermaid 图。',
            ]
          : [
              '4. 使用以下结构组织回答：`## 定义`、`## 核心特点`、`## 示例`、`## 常见误区或面试追问`、`## 一句话总结`。',
              '5. 概念题不要只给定义，还要补“为什么重要”或“实际使用场景”。',
            ];

    return [
      '当前用户命中了“前端技术问答”意图。',
      `问题：${intent.question}`,
      '请像资深前端面试官一样回答，不要像百科词条，也不要像固定模板。',
      '回复要求：',
      '1. 第一段先直接给结论，不要先寒暄。',
      '2. 不要使用 H1、不要使用 Markdown 分割线、不要使用 emoji 标题、不要输出 mermaid 图。',
      '3. 技术问答优先使用 `##` 和 `###` 组织内容，除非用户明确要求，否则不要使用表格；如果是对比题，只有在确实必要时才使用简短表格。',
      ...structureLines,
      '6. 如果需要代码，只给一个最小、可理解的短示例，不要堆太多长代码块。',
      '7. 最后一节必须补“常见误区 / 面试追问 / 面试回答建议”中的至少一项，并给一句自然的下一步引导。',
    ].join('\n');
  }

  return [
    '当前用户命中了“项目亮点提炼”高频意图。',
    '请围绕“业务价值、技术难点、个人贡献、量化结果”这些维度给出结构化建议。',
    '最后引导用户把具体项目经历贴出来，以便你继续改写成适合前端求职简历的表达。',
    '回复保持专业、紧凑，不要输出 Markdown 分割线。',
  ].join('\n');
}
